	public static function parse(string $string) : array {
		$idMeta = constant($string);
		if (!is_string($idMeta)) {
			throw new libBedrockUnexpectedValueException();
		}
		[$id, $meta] = explode(":", $idMeta);
		return [
			"id" => (int) $id,
			"meta" => (int) $meta,
		];
	}
}
